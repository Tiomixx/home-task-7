package a.Level.homeworkNo7.Computer;

public class Computer {

    private String brandName;
    private Double price;
    private String color;
    private String RAM;
    private String processor;
    private String hardDiskSize;
    private String displaySize;
    private String computerMemoryType;
    private String videocard;
    private Double weight;
    private String countryOfManufact;

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getRAM() {
        return RAM;
    }

    public void setRAM(String RAM) {
        this.RAM = RAM;
    }

    public String getProcessor() {
        return processor;
    }

    public void setProcessor(String processor) {
        this.processor = processor;
    }

    public String getHardDiskSize() {
        return hardDiskSize;
    }

    public void setHardDiskSize(String hardDiskSize) {
        this.hardDiskSize = hardDiskSize;
    }

    public String getDisplaySize() {
        return displaySize;
    }

    public void setDisplaySize(String displaySize) {
        this.displaySize = displaySize;
    }

    public String getComputerMemoryType() {
        return computerMemoryType;
    }

    public void setComputerMemoryType(String computerMemoryType) {
        this.computerMemoryType = computerMemoryType;
    }

    public String getVideocard() {
        return videocard;
    }

    public void setVideocard(String videocard) {
        this.videocard = videocard;
    }

    public Double getWeight() {
        return weight;
    }

    public void setWeight(Double weight) {
        this.weight = weight;
    }

    public String getCountryOfManufact() {
        return countryOfManufact;
    }

    public void setCountryOfManufact(String countryOfManufact) {
        this.countryOfManufact = countryOfManufact;
    }
}
